/*LES OPERATEURS ARITHMETIQUES*/

/* L ADDITION*/

    // DECLARATION DE PLUSIEURS VARIABLES A LA SUITE//

    var nb1, nb2, resultat;
    nb1 = 10;
    nb2 = 5;

    // Addition de nb1 + nb2 avec l'operateur " + "//
    resultat = nb1 + nb2;

    // Affichage du resultat dans la console
    console.log( "l'addition de nb1 + nb2 est egale à :" + resultat );

// LA SOUSTRACTION

    // la soustraction de nb1 et nb2 avec l'operateur " - "
    resultat = nb1 - nb2;

    // Affichage du resultat dans la console
    console.log( "la soustraction de nb1 - nb2 est egale à :" + resultat );

//-- LA MULTIPLICATION

    // La multiplication de nb1 et nb2 avec l'operateur " * "
    resultat = nb1 * nb2;

    // Affichage du resultat dans la console
    console.log( "la multiplication de nb1 * nb2 est egale à :" + resultat );

// LA DIVISION

    // La division de nb1 et nb2 avec l'operateur " / "
    resultat = nb1 / nb2;

    // Affichage du resultat dans la console
    console.log( "la division de nb1 / nb2 est egale à :" + resultat );

// LE MODULO

    // Le resultat du modulo correspond au reste de la division.
    
    // Le modulo de nb1 et nb2 avec l'operateur " % "
    nb1 = 11;
    resultat = nb1 % nb2;

    // Affichage du resultat dans la console
    console.log( "le modulo de nb1 % nb2 est egale à :" + resultat );

